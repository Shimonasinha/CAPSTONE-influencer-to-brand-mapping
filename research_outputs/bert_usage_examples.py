# EXAMPLE 7: Temporal Analysis
# ============================================================================

def analyze_temporal_trends(embeddings, metadata, time_column='published_at'):
    """
    Analyze how content themes evolve over time.
    
    Args:
        embeddings: BERT embeddings
        metadata: Metadata with timestamps
        time_column: Name of timestamp column
    
    Returns:
        Dictionary with temporal statistics
    """
    # Convert to datetime
    metadata['timestamp'] = pd.to_datetime(metadata[time_column])
    metadata['year_month'] = metadata['timestamp'].dt.to_period('M')
    
    # Cluster by time period
    temporal_analysis = {}
    
    for period in metadata['year_month'].unique():
        mask = metadata['year_month'] == period
        period_embeddings = embeddings[mask]
        
        # Compute centroid
        centroid = period_embeddings.mean(axis=0)
        
        temporal_analysis[str(period)] = {
            'count': mask.sum(),
            'centroid': centroid,
            'variance': period_embeddings.var(axis=0).mean()
        }
    
    return temporal_analysis


