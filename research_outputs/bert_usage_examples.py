# EXAMPLE 4: Brand-Influencer Matching
# ============================================================================

def compute_brand_alignment(brand_description, influencer_embeddings, 
                           model, tokenizer):
    """
    Compute semantic alignment between brand and influencer content.
    
    Args:
        brand_description: Brand description text
        influencer_embeddings: Embeddings of influencer content
        model: BERT model
        tokenizer: BERT tokenizer
    
    Returns:
        Array of similarity scores
    """
    import torch
    
    # Encode brand description
    inputs = tokenizer(brand_description, return_tensors='pt',
                      padding=True, truncation=True, max_length=512)
    
    with torch.no_grad():
        outputs = model(**inputs)
        brand_embedding = outputs.last_hidden_state[:, 0, :].numpy()
    
    # Compute similarities
    similarities = cosine_similarity(brand_embedding, influencer_embeddings)[0]
    
    return similarities


def find_matching_influencers(brand_description, embeddings, metadata,
                             model, tokenizer, top_k=20):
    """
    Find influencers whose content aligns with a brand.
    
    Args:
        brand_description: Brand description
        embeddings: All content embeddings
        metadata: Metadata with influencer info
        model: BERT model
        tokenizer: BERT tokenizer
        top_k: Number of top influencers
    
    Returns:
        DataFrame with top matching content
    """
    similarities = compute_brand_alignment(
        brand_description, embeddings, model, tokenizer
    )
    
    # Get top matches
    top_indices = np.argsort(similarities)[-top_k:][::-1]
    
    results = metadata.iloc[top_indices].copy()
    results['brand_alignment'] = similarities[top_indices]
    
    return results


