# ============================================================================

def train_sponsorship_detector(embeddings, labels):
    """
    Train a classifier to detect sponsored content.
    
    Args:
        embeddings: BERT embeddings
        labels: Binary labels (1=sponsored, 0=organic)
    
    Returns:
        Trained model
    """
    from sklearn.model_selection import train_test_split
    from sklearn.metrics import classification_report
    
    # Split data
    X_train, X_test, y_train, y_test = train_test_split(
        embeddings, labels, test_size=0.2, random_state=42
    )
    
    # Train classifier
    model = LogisticRegression(max_iter=1000)
    model.fit(X_train, y_train)
    
    # Evaluate
    y_pred = model.predict(X_test)
    print(classification_report(y_test, y_pred))
    
    return model


def predict_sponsorship(text, model, bert_model, tokenizer):
    """
    Predict if a text is sponsored content.
    
    Args:
        text: Input text
        model: Trained classifier
        bert_model: BERT model
        tokenizer: BERT tokenizer
    
    Returns:
        Probability of being sponsored
    """
    import torch
    
    # Extract BERT embedding
    inputs = tokenizer(text, return_tensors='pt',
                      padding=True, truncation=True, max_length=512)
    
    with torch.no_grad():
        outputs = bert_model(**inputs)
        embedding = outputs.last_hidden_state[:, 0, :].numpy()
    
    # Predict
    prob = model.predict_proba(embedding)[0, 1]
    
    return prob


