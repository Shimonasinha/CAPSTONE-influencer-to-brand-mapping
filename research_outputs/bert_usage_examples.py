# EXAMPLE 6: Multi-modal Integration (BERT + CLIP)
# ============================================================================

def combine_bert_clip(bert_embeddings, clip_embeddings):
    """
    Combine BERT textual and CLIP visual features.
    
    Args:
        bert_embeddings: BERT embeddings (N x 768)
        clip_embeddings: CLIP embeddings (N x 512)
    
    Returns:
        Combined embeddings (N x 1280)
    """
    # Simple concatenation
    combined = np.hstack([bert_embeddings, clip_embeddings])
    
    # Optional: Normalize each modality
    from sklearn.preprocessing import StandardScaler
    
    scaler_bert = StandardScaler()
    scaler_clip = StandardScaler()
    
    bert_norm = scaler_bert.fit_transform(bert_embeddings)
    clip_norm = scaler_clip.fit_transform(clip_embeddings)
    
    combined_norm = np.hstack([bert_norm, clip_norm])
    
    return combined, combined_norm


def weighted_multimodal_similarity(bert_emb1, clip_emb1, bert_emb2, clip_emb2,
                                   text_weight=0.6, visual_weight=0.4):
    """
    Compute weighted similarity using both text and visual features.
    
    Args:
        bert_emb1, bert_emb2: Text embeddings
        clip_emb1, clip_emb2: Visual embeddings
        text_weight: Weight for text similarity
        visual_weight: Weight for visual similarity
    
    Returns:
        Combined similarity score
    """
    text_sim = cosine_similarity(
        bert_emb1.reshape(1, -1),
        bert_emb2.reshape(1, -1)
    )[0, 0]
    
    visual_sim = cosine_similarity(
        clip_emb1.reshape(1, -1),
        clip_emb2.reshape(1, -1)
    )[0, 0]
    
    combined_sim = text_weight * text_sim + visual_weight * visual_sim
    
    return combined_sim


