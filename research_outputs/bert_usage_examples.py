# EXAMPLE 2: Semantic Similarity Search
# ============================================================================

def find_similar_texts(query_idx, embeddings, metadata, top_k=10):
    """
    Find texts most similar to a query text.
    
    Args:
        query_idx: Index of query text
        embeddings: BERT embeddings array
        metadata: Metadata DataFrame
        top_k: Number of similar texts to return
    
    Returns:
        DataFrame with similar texts and similarity scores
    """
    # Get query embedding
    query_embedding = embeddings[query_idx].reshape(1, -1)
    
    # Compute similarities
    similarities = cosine_similarity(query_embedding, embeddings)[0]
    
    # Get top-k indices (excluding query itself)
    top_indices = np.argsort(similarities)[-(top_k+1):-1][::-1]
    
    # Create result DataFrame
    results = metadata.iloc[top_indices].copy()
    results['similarity'] = similarities[top_indices]
    
    return results


def semantic_search(query_text, model, tokenizer, embeddings, metadata, top_k=10):
    """
    Search for texts semantically similar to a query string.
    
    Args:
        query_text: Text string to search for
        model: BERT model
        tokenizer: BERT tokenizer
        embeddings: Corpus embeddings
        metadata: Metadata DataFrame
        top_k: Number of results
    
    Returns:
        DataFrame with matching texts
    """
    import torch
    
    # Encode query
    inputs = tokenizer(query_text, return_tensors='pt', 
                      padding=True, truncation=True, max_length=512)
    
    with torch.no_grad():
        outputs = model(**inputs)
        query_embedding = outputs.last_hidden_state[:, 0, :].numpy()
    
    # Find similar
    similarities = cosine_similarity(query_embedding, embeddings)[0]
    top_indices = np.argsort(similarities)[-top_k:][::-1]
    
    results = metadata.iloc[top_indices].copy()
    results['similarity'] = similarities[top_indices]
    
    return results


