# EXAMPLE 3: Content Clustering
# ============================================================================

def cluster_content(embeddings, n_clusters=10):
    """
    Cluster content based on semantic similarity.
    
    Args:
        embeddings: BERT embeddings
        n_clusters: Number of clusters
    
    Returns:
        Cluster labels and model
    """
    kmeans = KMeans(n_clusters=n_clusters, random_state=42, n_init=20)
    labels = kmeans.fit_predict(embeddings)
    
    return labels, kmeans


def analyze_clusters(embeddings, metadata, labels):
    """
    Analyze characteristics of each cluster.
    
    Args:
        embeddings: BERT embeddings
        metadata: Metadata DataFrame
        labels: Cluster labels
    
    Returns:
        Dictionary with cluster statistics
    """
    cluster_stats = {}
    
    for cluster_id in range(labels.max() + 1):
        mask = labels == cluster_id
        cluster_data = metadata[mask]
        
        cluster_stats[cluster_id] = {
            'size': mask.sum(),
            'platforms': cluster_data['platform'].value_counts().to_dict(),
            'sample_texts': cluster_data['cleaned_text'].sample(
                n=min(5, len(cluster_data))
            ).tolist()
        }
    
    return cluster_stats


